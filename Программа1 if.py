a = int(input("Введите любое число \n")) #Ввод числа с клавиатуры, а также добавляем табуляцию для удобности ввода.
if a > 0: #проверяем, положительна ли введенная переменная, если да то прибавляем единицу к введенной переменной
    print(a + 1) #реализуем прибавление единицы к  введённому числу и выводим
else: #если число отрицательно, то выводим число без изменений
    print(a) # Реализуем вывод число без всяких изменений
