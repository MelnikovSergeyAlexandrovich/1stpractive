            number_x = float(input("Введите число\n"))  # Ввод числа с плавающей запятой
            Pow = float(input("Введите степень\n"))  # Ввод степени N  с плавающей запятой
            print(number_x ** Pow)  # Вывод результата возведения данного числа в данную степень N
