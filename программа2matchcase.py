if '1' <= operation <= '9':  # Реализуем оператор для выполнения действия при том условии, если введенное число будет больше или равно 1 и меньше или равно 9
    match operation:  # Реализуем оператор выбора, в котором каждый блок выполняет определенную операцию в зависимости от выбора пользователя

        case '1':  # Реализуем первый блок в котором будет происходить сложение данных чисел
            number_one = float(input("Введите первое число\n"))  # Ввод первого числа с плавающей запятой
            number_two = float(input("Введите второе число\n"))  # Ввод второго числа с плавающей запятой
            print(number_one + number_two)  # Вывод результата суммы данных двух чисел
        case '2':  # Реализуем второй блок в котором будет происходить разность данных чисел
            number_one = float(input("Введите первое число\n"))  # Ввод первого числа с плавающей запятой
            number_two = float(input("Введите второе число\n"))  # Ввод второго числа с плавающей запятой
            print(number_one - number_two)  # Вывод результата разности данных двух чисел
        case '3':  # Реализуем третий блок в котором будет происходить умножение данных чисел
            number_one = float(input("Введите первое число\n"))  # Ввод первого числа с плавающей запятой
            number_two = float(input("Введите второе число\n"))  # Ввод второго числа с плавающей запятой
            print(number_one * number_two)  # Вывод резульатата произведения данных двух чисел
        case '4':  # Реализуем четвертый блок в котором будет происходить деление данных чисел
            number_one = float(input("Введите первое число\n"))  # Ввод первого числа с плавающей запятой
            number_two = float(input("Введите второе число\n"))  # Ввод второго числа с плавающей запятой
            print(number_one / number_two)  # Вывод результата деления данных двух чисел
